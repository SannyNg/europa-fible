// Get current language from localStorage or browser
function getCurrentLanguage() {
    const stored = localStorage.getItem('fibel-language');
    if (stored && translations[stored]) {
        return stored;
    }

    const browserLang = navigator.language.split('-')[0];
    if (translations[browserLang]) {
        return browserLang;
    }

    return 'de'; // Default to German
}

// Set language
function setLanguage(lang) {
    if (!translations[lang]) {
        console.warn(`Language "${lang}" not available, falling back to German`);
        lang = 'de';
    }

    localStorage.setItem('fibel-language', lang);
    document.documentElement.lang = lang;

    applyTranslations(lang);
}

// Apply translations to all elements
function applyTranslations(lang) {
    const t = translations[lang];
    if (!t) return;

    // Merge with German as fallback
    const fallback = translations['de'];

    // Translate all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const text = t[key] || fallback[key];
        if (text) {
            if (key === 'warning_text') {
                el.innerHTML = text;
            } else {
                el.textContent = text;
            }
        }
    });

    // Translate placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const text = t[key] || fallback[key];
        if (text) {
            el.placeholder = text;
        }
    });

    // Update language selector
    const langSelect = document.getElementById('language-select');
    if (langSelect) {
        langSelect.value = lang;
    }
}

// Get translation
function t(key) {
    const lang = getCurrentLanguage();
    return translations[lang]?.[key] || translations['de']?.[key] || key;
}

// Initialize on DOM ready
document.addEventListener('DOMContentLoaded', () => {
    const currentLang = getCurrentLanguage();
    setLanguage(currentLang);

    // Language selector handler
    const langSelect = document.getElementById('language-select');
    if (langSelect) {
        langSelect.value = currentLang;
        langSelect.addEventListener('change', (e) => {
            setLanguage(e.target.value);
        });
    }
});

// Export for use in other modules
window.i18n = {
    t,
    setLanguage,
    getCurrentLanguage,
    translations
};
